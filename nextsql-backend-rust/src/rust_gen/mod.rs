/// Generate Rust code from a parsed NextSQL module.
pub fn generate_rust_module(
    module: &Module,
    schema: &DatabaseSchema,
    module_name: &str,
) -> GeneratedRustFile {
    let result = generate_rust_file(module, schema);
    GeneratedRustFile {
        filename: format!("{}.rs", module_name),
        content: result.content,
        errors: result.errors,
    }
}

/// Generate a shared types.rs file containing all valtype definitions from multiple modules.
/// This avoids duplicating valtype structs across generated files.
pub fn generate_valtype_file(modules: &[&Module]) -> GeneratedRustFile {
    let mut out = String::new();
    out.push_str("// AUTO-GENERATED by nextsql. Do not edit.\n\n");

    // Collect all valtypes across modules, deduplicating by name
    let mut all_valtypes: HashMap<String, BuiltInType> = HashMap::new();
    for module in modules {
        for tl in &module.toplevels {
            if let TopLevel::ValType(vt) = tl {
                all_valtypes.entry(vt.name.clone())
                    .or_insert_with(|| vt.base_type.clone());
            }
        }
    }

    // Sort by name for deterministic output
    let mut sorted: Vec<(&String, &BuiltInType)> = all_valtypes.iter().collect();
    sorted.sort_by_key(|(name, _)| (*name).clone());

    for (name, base_type) in sorted {
        let rust_base = nextsql_type_to_rust(&Type::BuiltIn(base_type.clone()));
        out.push_str(&format!("pub struct {}(pub {});\n\n", name, rust_base));
        out.push_str(&format!("impl super::runtime::ToSqlParam for {} {{\n", name));
        out.push_str("    fn as_any(&self) -> &(dyn std::any::Any + Send + Sync) {\n");
        out.push_str("        &self.0\n");
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }

    GeneratedRustFile {
        filename: "types.rs".to_string(),
        content: out,
        errors: Vec::new(),
    }
}

/// Generate the full .rs file content from a module and schema.
/// Includes valtype definitions inline (for backward compatibility / single-file use).
pub fn generate_rust_file(module: &Module, schema: &DatabaseSchema) -> GeneratedRustFile {
    generate_rust_file_with_options(module, schema, false)
}

/// Generate the full .rs file content from a module and schema.
/// When `skip_valtype_generation` is true, valtype structs are not emitted inline
/// (they should be generated in a shared types.rs instead).
pub fn generate_rust_file_with_options(module: &Module, schema: &DatabaseSchema, skip_valtype_generation: bool) -> GeneratedRustFile {
    generate_rust_file_full(module, schema, skip_valtype_generation, &NamingConfig::default())
}

/// Generate the full .rs file content with all options including naming config.
pub fn generate_rust_file_full(module: &Module, schema: &DatabaseSchema, skip_valtype_generation: bool, naming: &NamingConfig) -> GeneratedRustFile {
    let mut out = String::new();
    let mut errors: Vec<String> = Vec::new();
    out.push_str("// AUTO-GENERATED by nextsql. Do not edit.\n\n");

    if skip_valtype_generation {
        out.push_str("use super::types::*;\n\n");
    }

    // Build the valtype registry from the module
    let registry = ValTypeRegistry::from_module(module);

    // Build the relation registry from the module
    let rel_registry = sql_gen::RelationRegistry::from_module(module);

    // Build the input type registry from the module
    let input_registry = InputTypeRegistry::from_module(module);

    // Generate valtype newtype structs (only if not skipping)
    if !skip_valtype_generation {
        generate_valtype_structs(&mut out, &registry);
    }

    // Generate Rust enums for PostgreSQL enum types in the schema
    generate_db_enums(&mut out, schema);

    // Emit input type structs
    for toplevel in &module.toplevels {
        if let TopLevel::Input(input) = toplevel {
            emit::emit_input_struct(&mut out, input, &registry, schema);
        }
    }

    // First pass: generate queries/mutations into a buffer, collecting model table names
    let mut body = String::new();
    let mut model_tables: HashSet<String> = HashSet::new();

    for toplevel in &module.toplevels {
        match toplevel {
            TopLevel::Query(q) => generate_query(&mut body, q, schema, &mut model_tables, &registry, &rel_registry, &mut errors, &input_registry),
            TopLevel::Mutation(m) => generate_mutation(&mut body, m, schema, &mut model_tables, &registry, &rel_registry, naming, &input_registry),
            // With, Relation, ValType, and Input are not directly translated to Rust code here
            TopLevel::With(_) | TopLevel::Relation(_) | TopLevel::ValType(_) | TopLevel::Input(_) => {}
        }
    }

    // Generate model structs at the top for tables that need them
    let mut sorted_tables: Vec<&String> = model_tables.iter().collect();
    sorted_tables.sort();
    for table_name in sorted_tables {
        if let Some(table_schema) = schema.get_table(table_name) {
            generate_model_struct(&mut out, table_schema, &registry, schema);
        }
    }

    // Append the query/mutation code
    out.push_str(&body);

    GeneratedRustFile {
        filename: String::new(),
        content: out,
        errors,
    }
}

/// Generate Rust enum definitions for all PostgreSQL enum types in the schema.
fn generate_db_enums(out: &mut String, schema: &DatabaseSchema) {
    for (enum_name, enum_schema) in &schema.enums {
        let rust_name = to_pascal_case_type(enum_name);

        // Enum definition
        out.push_str("#[derive(Debug, Clone, PartialEq)]\n");
        out.push_str(&format!("pub enum {} {{\n", rust_name));
        for variant in &enum_schema.variants {
            out.push_str(&format!("    {},\n", to_pascal_case_type(variant)));
        }
        out.push_str("}\n\n");

        // Display impl (for SQL parameter binding - converts to the PostgreSQL string value)
        out.push_str(&format!("impl std::fmt::Display for {} {{\n", rust_name));
        out.push_str("    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {\n");
        out.push_str("        match self {\n");
        for variant in &enum_schema.variants {
            out.push_str(&format!(
                "            {}::{} => write!(f, \"{}\"),\n",
                rust_name,
                to_pascal_case_type(variant),
                variant,
            ));
        }
        out.push_str("        }\n");
        out.push_str("    }\n");
        out.push_str("}\n\n");

        // FromStr impl (for parsing from database strings)
        out.push_str(&format!("impl std::str::FromStr for {} {{\n", rust_name));
        out.push_str("    type Err = String;\n");
        out.push_str("    fn from_str(s: &str) -> Result<Self, Self::Err> {\n");
        out.push_str("        match s {\n");
        for variant in &enum_schema.variants {
            out.push_str(&format!(
                "            \"{}\" => Ok({}::{}),\n",
                variant,
                rust_name,
                to_pascal_case_type(variant),
            ));
        }
        out.push_str(&format!(
            "            _ => Err(format!(\"Unknown {} variant: {{}}\", s)),\n",
            rust_name
        ));
        out.push_str("        }\n");
        out.push_str("    }\n");
        out.push_str("}\n\n");

        // ToSqlParam impl
        out.push_str(&format!("impl super::runtime::ToSqlParam for {} {{\n", rust_name));
        out.push_str("    fn as_any(&self) -> &(dyn std::any::Any + Send + Sync) {\n");
        out.push_str("        self\n");
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }
}

