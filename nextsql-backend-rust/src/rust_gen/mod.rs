/// Registry of all input type definitions.
#[derive(Debug, Clone)]
pub(crate) struct InputTypeRegistry {
    pub input_types: HashMap<String, InputType>,
}

impl InputTypeRegistry {
    pub fn from_module(module: &Module) -> Self {
        let mut input_types = HashMap::new();
        for tl in &module.toplevels {
            if let TopLevel::Input(input) = tl {
                input_types.insert(input.name.clone(), input.clone());
            }
        }
        InputTypeRegistry { input_types }
    }

    pub fn get(&self, name: &str) -> Option<&InputType> {
        self.input_types.get(name)
    }
}

/// Extract all ValType definitions from a module (for CLI use).
pub fn extract_valtypes(module: &Module) -> Vec<&ValType> {
    module.toplevels.iter().filter_map(|tl| {
        match tl {
            TopLevel::ValType(vt) => Some(vt),
            _ => None
        }
    }).collect()
}

/// Generated Rust code for a single .nsql file
#[derive(Debug)]
pub struct GeneratedRustFile {
    pub filename: String,
    pub content: String,
    pub errors: Vec<String>,
}

/// A single field in a generated Row struct.
#[derive(Clone)]
struct RowField {
    name: String,
    rust_type: String,
    getter: String,
    /// If set, wrap the getter call with this valtype constructor.
    valtype_wrapper: Option<String>,
    /// If true, the field is a parsed enum type (use .parse().unwrap() on the string getter).
    enum_parse: bool,
}

/// Configuration for struct naming patterns.
#[derive(Debug, Clone)]
pub struct NamingConfig {
    /// Pattern for insert param structs, e.g. "Insert{Table}Params"
    pub insert_params_pattern: String,
    /// Pattern for update changeset structs, e.g. "Update{Table}Params"
    pub update_params_pattern: String,
}

impl Default for NamingConfig {
    fn default() -> Self {
        Self {
            insert_params_pattern: "Insert{Table}Params".to_string(),
            update_params_pattern: "Update{Table}Params".to_string(),
        }
    }
}

impl NamingConfig {
    /// Apply the insert naming pattern with the given table name (PascalCase singular).
    pub fn insert_struct_name(&self, table_pascal: &str) -> String {
        self.insert_params_pattern.replace("{Table}", table_pascal)
    }

    /// Apply the update naming pattern with the given table name (PascalCase singular).
    pub fn update_struct_name(&self, table_pascal: &str) -> String {
        self.update_params_pattern.replace("{Table}", table_pascal)
    }
}

